namespace GLFW.Net
{
    /// <summary>
    /// Standard cursor shapes.
    /// </summary>
    public enum CursorShape
    {
        /// <summary>
        /// The regular arrow cursor shape.
        /// </summary>
        Arrow = 0x00036001,
        
        /// <summary>
        /// The text input I-beam cursor shape.
        /// </summary>
        IBeam = 0x00036002,
        
        /// <summary>
        /// The crosshair shape.
        /// </summary>
        Crosshair = 0x00036003,
        
        /// <summary>
        /// The hand shape.
        /// </summary>
        Hand = 0x00036004,
        
        /// <summary>
        /// The horizontal resize arrow shape.
        /// </summary>
        HResize = 0x00036005,
        
        /// <summary>
        /// The vertical resize arrow shape.
        /// </summary>
        VResize = 0x00036006
    }
}
